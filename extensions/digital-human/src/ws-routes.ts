/**
 * @fileoverview ws-routes — registers the digital-human WebSocket route on the
 * WinClaw Gateway and wires inbound messages to the correct
 * {@link SessionManager} / {@link RealtimeSessionHandler} methods.
 *
 * Design reference: §8.5 of winclaw-avatar-digital-human-realtime-voice-plan.md
 *
 * Route registered:
 *   WS  /api/dh/connect/:sessionToken
 *
 * Protocol — inbound messages from the browser (JSON-encoded):
 * ```jsonc
 * // Audio chunk (PCM16 LE 16 kHz mono, base64-encoded)
 * { "type": "audio", "data": "<base64>" }
 *
 * // Video frame (JPEG/PNG, base64-encoded)
 * { "type": "video", "data": "<base64>" }
 *
 * // User text message
 * { "type": "text", "text": "帮我查明天的天气" }
 *
 * // Keep-alive ping
 * { "type": "ping" }
 *
 * // Client-initiated session stop
 * { "type": "stop" }
 * ```
 *
 * Protocol — outbound messages to the browser (JSON-encoded):
 * ```jsonc
 * { "type": "session.created", "sessionId": "...", "config": { ... } }
 * { "type": "dh_stream_info", "data": { "liveId": "...", ... } }
 * { "type": "ai_audio",  "data": { "audio": "<base64>", "format": "pcm16", "sample_rate": 24000 } }
 * { "type": "ai_text",   "data": { "content": "...", "is_delta": true|false } }
 * { "type": "user_transcript", "data": { "content": "..." } }
 * { "type": "ai_response_started" }
 * { "type": "ai_response_done" }
 * { "type": "pong" }
 * { "type": "error", "code": "...", "message": "...", "sessionId": "..." }
 * ```
 *
 * Authentication:
 *   The `:sessionToken` path parameter is validated against the WinClaw
 *   Gateway auth layer.  The `authenticateSessionToken` function in this file
 *   is the integration point; replace the stub with your actual auth logic.
 */

import type { IncomingMessage } from "node:http";
import WebSocket from "ws";
import type { RawData as WsRawData } from "ws";

import { SessionManager } from "./session-manager.js";
import type { SessionManagerConfig } from "./session-manager.js";
import type { DigitalHumanConfig } from "./config.js";

export { SessionManager };
export type { SessionManagerConfig };

// ---------------------------------------------------------------------------
// Gateway API surface (types provided by the WinClaw plugin-sdk at runtime)
// ---------------------------------------------------------------------------

/**
 * Minimal subset of the WinClaw Gateway API required to register routes.
 * Replace with the concrete type from `winclaw/plugin-sdk` when available.
 */
export interface GatewayAPI {
  /**
   * Register a WebSocket upgrade route on the gateway HTTP server.
   * Path patterns may use `:param` named segments.
   *
   * @param path    - Route path pattern (e.g. `"/api/dh/connect/:sessionToken"`).
   * @param handler - Handler invoked once per upgrade.
   */
  registerWsRoute(
    path: string,
    handler: (ctx: WsRouteContext) => Promise<void> | void,
  ): void;
}

/** Context passed to a WebSocket route handler. */
export interface WsRouteContext {
  /** The fully upgraded WebSocket connection. */
  socket: WebSocket;
  /** The triggering HTTP upgrade request. */
  req: IncomingMessage;
  /** Named path parameters extracted from the route pattern. */
  params: Record<string, string>;
}

// ---------------------------------------------------------------------------
// Authentication stub
// ---------------------------------------------------------------------------

/**
 * Result of session-token authentication.
 */
interface AuthResult {
  /** `true` when the token is valid. */
  valid: boolean;
  /** Client identifier (e.g. user ID) when valid; `undefined` otherwise. */
  clientId?: string;
}

/**
 * Validate a session token from the route path parameter.
 *
 * **Replace this stub** with your actual authentication logic (e.g. JWT
 * verification against the WinClaw API Key store or Gateway session table).
 *
 * The current implementation accepts any non-empty token to allow development
 * without a live auth backend.  In production, tokens should be single-use
 * short-lived JWTs generated by `POST /api/dh/session` on the HTTP layer.
 *
 * @param token - The raw `:sessionToken` value from the URL path.
 * @param req   - The HTTP upgrade request (may carry auth headers).
 * @returns     `{ valid: true, clientId: ... }` or `{ valid: false }`.
 */
async function authenticateSessionToken(
  token: string,
  _req: IncomingMessage,
): Promise<AuthResult> {
  if (!token || token.length < 8) {
    return { valid: false };
  }
  // TODO: replace with real JWT / API-Key validation.
  return { valid: true, clientId: `client-${token.slice(0, 8)}` };
}

