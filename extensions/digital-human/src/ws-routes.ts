/**
 * Register the digital-human WebSocket route on the WinClaw Gateway.
 *
 * Creates a shared {@link SessionManager} instance that persists for the
 * lifetime of the Gateway process.  Each inbound WebSocket upgrade creates a
 * new {@link RealtimeSessionHandler} inside the manager.
 *
 * The manager's timeout checker is started immediately and must be stopped
 * via the returned `dispose()` function when the Gateway shuts down.
 *
 * @param gateway - The Gateway API object provided by the WinClaw plugin SDK.
 * @param config  - Validated plugin configuration.
 * @param managerConfig - Full dependency set for {@link SessionManager}.
 * @returns A `dispose()` function that shuts down all sessions and stops
 *          the timeout checker — call this on Gateway exit.
 *
 * @example
 * ```typescript
 * const dispose = registerDigitalHumanRoutes(gateway, config, {
 *   config,
 *   workspaceDir: '/home/user/.winclaw/workspace',
 *   agentRunner,
 *   memoryPlugin,
 *   agentId: 'digital-human',
 * });
 *
 * process.on('SIGTERM', async () => {
 *   await dispose();
 *   process.exit(0);
 * });
 * ```
 */
/** Return value of {@link registerDigitalHumanRoutes}. */
export interface DigitalHumanRouteHandle {
  /** Shuts down all sessions and stops the timeout checker. */
  dispose: () => Promise<void>;
  /** The shared SessionManager instance (used by HTTP admin routes). */
  manager: SessionManager;
}

export function registerDigitalHumanRoutes(
  gateway: GatewayAPI,
  config: DigitalHumanConfig,
  managerConfig: SessionManagerConfig,
): DigitalHumanRouteHandle {
  const manager = new SessionManager(managerConfig);
  manager.startTimeoutChecker();

  // ── WS /api/dh/connect/:sessionToken ─────────────────────────────────────
  gateway.registerWsRoute(
    "/api/dh/connect/:sessionToken",
    async (ctx: WsRouteContext) => {
      const { socket, req, params } = ctx;
      const sessionToken = params["sessionToken"] ?? "";
      const clientIp = getClientIp(req);

      // ── Authentication ──────────────────────────────────────────────────
      const auth = await authenticateSessionToken(sessionToken, req);
      if (!auth.valid) {
        console.warn(
          `[ws-routes] Rejected unauthorized connection from ${clientIp} (token: ${sessionToken.slice(0, 8)}…)`,
        );
        safeSend(socket, {
          type: "error",
          code: "UNAUTHORIZED",
          message: "Invalid or expired session token",
        });
        socket.close(4001, "Unauthorized");
        return;
      }

      // ── Concurrent session cap check ────────────────────────────────────
      if (manager.activeCount >= config.session.maxConcurrent) {
        console.warn(
          `[ws-routes] Rejected connection: concurrent limit ${config.session.maxConcurrent} reached`,
        );
        safeSend(socket, {
          type: "error",
          code: "CONCURRENT_LIMIT",
          message: `Maximum concurrent sessions (${config.session.maxConcurrent}) reached`,
        });
        socket.close(1008, "Concurrent session limit reached");
        return;
      }

      // ── Create session ──────────────────────────────────────────────────
      const sessionId = generateSessionId();

      console.info(
        `[ws-routes] New connection: sessionId=${sessionId} clientId=${auth.clientId} ip=${clientIp}`,
      );

      // Send the session-created acknowledgement before initializing so the
      // client knows its assigned session ID while we connect to Qwen / DH.
      safeSend(socket, {
        type: "session.created",
        sessionId,
        config: {
          voice: config.qwen.voice,
          model: config.qwen.model,
          role: config.bytedance.role,
          timeoutMinutes: config.session.timeoutMinutes,
          maxConcurrent: config.session.maxConcurrent,
        },
      });

      // Initialize the session handler (connects to Qwen + ByteDance DH).
      let handler: ReturnType<typeof manager.getSession>;
      try {
        await manager.startSession(sessionId, socket);
        handler = manager.getSession(sessionId);
      } catch (err) {
        const message =
          err instanceof Error ? err.message : "Session initialization failed";
        console.error(
          `[ws-routes] Failed to start session ${sessionId}:`,
          err,
        );
        safeSend(socket, {
          type: "error",
          code: "SESSION_INIT_FAILED",
          message,
          sessionId,
        });
        socket.close(1011, "Session initialization failed");
        return;
      }

      // ── Message routing loop ────────────────────────────────────────────
      socket.on("message", (raw: WsRawData) => {
        // Register activity for the inactivity timeout.
        manager.touchActivity(sessionId);

        let parsed: unknown;
        try {
          parsed = JSON.parse(raw.toString());
        } catch {
          safeSend(socket, {
            type: "error",
            code: "INVALID_JSON",
            message: "Message payload is not valid JSON",
            sessionId,
          });
          return;
        }

        const msg = parseInboundMessage(parsed);
        if (!msg) {
          safeSend(socket, {
            type: "error",
            code: "UNKNOWN_MESSAGE_TYPE",
            message: `Unrecognised message type: ${(parsed as Record<string, unknown>)?.["type"] ?? "unknown"}`,
            sessionId,
          });
          return;
        }

        const currentHandler = manager.getSession(sessionId);
        if (!currentHandler) return; // Session already cleaned up.

        switch (msg.type) {
          case "audio":
            currentHandler.handleAudioMessage(msg.data);
            break;

          case "video":
            currentHandler.handleVideoMessage(msg.data);
            break;

          case "text":
            currentHandler.handleTextMessage(msg.text);
            break;

          case "ping":
            safeSend(socket, { type: "pong", sessionId });
            break;

          case "stop":
            // Client-initiated graceful disconnect.
            socket.close(1000, "Client requested session stop");
            break;
        }
      });

      // ── Graceful disconnect ─────────────────────────────────────────────
      socket.on("close", (code: number, reason: Buffer) => {
        const reasonStr = reason.toString();
        console.info(
          `[ws-routes] Session ${sessionId} closed: code=${code} reason="${reasonStr}"`,
        );
        // stopSession is idempotent — safe even if already removed by timeout.
        manager.stopSession(sessionId).catch((err) => {
          console.error(
            `[ws-routes] Error stopping session ${sessionId} on close:`,
            err,
          );
        });
      });

      // ── Error handling ──────────────────────────────────────────────────
      socket.on("error", (err: Error) => {
        console.error(
          `[ws-routes] WebSocket error on session ${sessionId}:`,
          err,
        );
        // Attempt to inform the client before the socket is torn down.
        safeSend(socket, {
          type: "error",
          code: "WEBSOCKET_ERROR",
          message: err.message,
          sessionId,
        });
        manager.stopSession(sessionId).catch((stopErr) => {
          console.error(
            `[ws-routes] Error stopping session ${sessionId} after WS error:`,
            stopErr,
          );
        });
      });

      // Suppress the linting warning — handler is used inside socket callbacks.
      void handler;
    },
  );

  // Return handle with dispose + manager reference.
  return {
    dispose: async (): Promise<void> => {
      console.info("[ws-routes] Gateway shutdown: stopping all sessions…");
      await manager.shutdownAll();
      manager.stopTimeoutChecker();
      console.info("[ws-routes] All digital-human sessions stopped");
    },
    manager,
  };
}
