/**
 * Generate a cryptographically random session ID.
 *
 * Uses `crypto.randomUUID()` when available (Node 19+); falls back to a
 * timestamp + random combination for older runtimes.
 */
function generateSessionId(): string {
  if (typeof crypto !== "undefined" && typeof crypto.randomUUID === "function") {
    return crypto.randomUUID();
  }
  return `dh-${Date.now().toString(36)}-${Math.random().toString(36).slice(2, 10)}`;
}

/**
 * Safely send a JSON message over a WebSocket, swallowing errors.
 *
 * @param ws  - Target WebSocket.
 * @param msg - Any JSON-serialisable value.
 */
function safeSend(ws: WebSocket, msg: unknown): void {
  try {
    if (ws.readyState === WebSocket.OPEN) {
      ws.send(JSON.stringify(msg));
    }
  } catch (err) {
    console.error("[ws-routes] safeSend error:", err);
  }
}

/**
 * Extract the remote IP address from an HTTP request, respecting common
 * reverse-proxy headers.
 *
 * @param req - Incoming HTTP request.
 */
function getClientIp(req: IncomingMessage): string {
  const forwarded = req.headers["x-forwarded-for"];
  if (typeof forwarded === "string") {
    return forwarded.split(",")[0]?.trim() ?? "unknown";
  }
  return req.socket.remoteAddress ?? "unknown";
}

// ---------------------------------------------------------------------------
// Inbound message types
// ---------------------------------------------------------------------------

/** Discriminated union of all message shapes the browser may send. */
export type InboundMessage =
  | { type: "audio"; data: string }
  | { type: "video"; data: string }
  | { type: "text"; text: string }
  | { type: "ping" }
  | { type: "stop" };

/**
 * Validate and narrow a raw parsed object to {@link InboundMessage}.
 *
 * Returns `null` for unrecognised or malformed messages.
 *
 * @param raw - The result of `JSON.parse` on the WebSocket payload.
 */
export function parseInboundMessage(raw: unknown): InboundMessage | null {
  if (!raw || typeof raw !== "object") return null;
  const msg = raw as Record<string, unknown>;

  switch (msg["type"]) {
    case "audio":
      if (typeof msg["data"] === "string") {
        return { type: "audio", data: msg["data"] };
      }
      return null;

    case "video":
      if (typeof msg["data"] === "string") {
        return { type: "video", data: msg["data"] };
      }
      return null;

    case "text":
      if (typeof msg["text"] === "string") {
        return { type: "text", text: msg["text"] };
      }
      return null;

    case "ping":
      return { type: "ping" };

    case "stop":
      return { type: "stop" };

    default:
      return null;
  }
}

